package com.digis01.CAlvarezProgramacionNCapasOctubre2025.ML;

public class Pais {
    
    private int IdPais;
    private String Nombre;
    
    public Pais(){}
    
    // Getters y Setters
    public int getIdPais() {
        return IdPais;
    }
    public void setIdPais(int IdPais) {
        this.IdPais = IdPais;
    }
    public String getNombre() {
        return Nombre;
    }
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
}
